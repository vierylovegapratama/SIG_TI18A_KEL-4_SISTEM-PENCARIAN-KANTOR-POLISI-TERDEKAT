        <p>Sebelum Kemerdekaan Indonesia</p>
        <p>Jaman Kerajaan
            Pada zaman Kerajaan Majapahit patih Gajah Mada membentuk pasukan 
            pengamanan yang disebut dengan Bhayangkara yang bertugas melindungi 
            raja dan kerajaan.</p>
         <p>Masa kolonial Belanda
            Pada masa kolonial Belanda, pembentukan pasukan keamanan diawali oleh
            pembentukan pasukan-pasukan jaga yang diambil dari orang-orang pribumi
            untuk menjaga aset dan kekayaan orang-orang Eropa di Hindia Belanda 
            pada waktu itu. Pada tahun 1867 sejumlah warga Eropa di Semarang, 
            merekrut 78 orang pribumi untuk menjaga keamanan mereka.
        <p>Wewenang operasional kepolisian ada pada residen yang dibantu asisten residen. 
           Rechts politie dipertanggungjawabkan pada procureur generaal (jaksa agung). 
           Pada masa Hindia Belanda terdapat bermacam-macam bentuk kepolisian, 
           seperti veld politie (polisi lapangan) , stands politie (polisi kota), 
           cultur politie (polisi pertanian), bestuurs politie (polisi pamong praja), 
           dan lain-lain.</p>
        <p>Sejalan dengan administrasi negara waktu itu, pada kepolisian juga diterapkan
           pembedaan jabatan bagi bangsa Belanda dan pribumi. Pada dasarnya pribumi tidak 
           diperkenankan menjabat hood agent (bintara), inspekteur van politie, dan commisaris 
           van politie. Untuk pribumi selama menjadi agen polisi diciptakan jabatan seperti mantri 
           polisi, asisten wedana, dan wedana polisi.</p>
        <p>Kepolisian modern Hindia Belanda yang dibentuk antara tahun 1897-1920 adalah merupakan 
           cikal bakal dari terbentuknya Kepolisian Negara Republik Indonesia saat ini.</p>
        <p>Masa pendudukan Jepang
           Pada masa ini Jepang membagi wiliyah kepolisian Indonesia menjadi Kepolisian Jawa dan 
           Madura yang berpusat di Jakarta, Kepolisian Sumatera yang berpusat di Bukittinggi, 
           Kepolisian wilayah Indonesia Timur berpusat di Makassar dan Kepolisian Kalimantan yang 
           berpusat di Banjarmasin.</p>
        <p>Tiap-tiap kantor polisi di daerah meskipun dikepalai oleh seorang pejabat kepolisian 
           bangsa Indonesia, tapi selalu didampingi oleh pejabat Jepang yang disebut sidookaan 
           yang dalam praktik lebih berkuasa dari kepala polisi.</p>
        <p>Awal Kemerdekaan Indonesia</p>
        <p>Periode 1945-1950
           Tidak lama setelah Jepang menyerah tanpa syarat kepada Sekutu, pemerintah militer Jepang
           membubarkan Peta dan Gyu-Gun, sedangkan polisi tetap bertugas, termasuk waktu Soekarno-Hatta 
           memproklamasikan kemerdekaan Indonesia pada tanggal 17 Agustus 1945. Secara resmi kepolisian 
           menjadi kepolisian Indonesia yang merdeka.</p>
        <p>Inspektur Kelas I (Letnan Satu) Polisi Mochammad Jassin, Komandan Polisi di Surabaya, 
           pada tanggal 21 Agustus 1945 memproklamasikan Pasukan Polisi Republik Indonesia sebagai 
           langkah awal yang dilakukan selain mengadakan pembersihan dan pelucutan senjata terhadap 
           tentara Jepang yang kalah perang, juga membangkitkan semangat moral dan patriotik seluruh rakyat 
           maupun satuan-satuan bersenjata yang sedang dilanda depresi dan kekalahan perang yang panjang. 
           Sebelumnya pada tanggal 19 Agustus 1945 dibentuk Badan Kepolisian Negara (BKN) oleh 
           Panitia Persiapan Kemerdekaan Indonesia (PPKI). Pada tanggal 29 September 1945 Presiden Soekarno 
           melantik R.S. Soekanto Tjokrodiatmodjo menjadi Kepala Kepolisian Negara (KKN).</p>
        <p>Pada awalnya kepolisian berada dalam lingkungan Kementerian Dalam Negeri dengan nama Djawatan 
           Kepolisian Negara yang hanya bertanggung jawab masalah administrasi, sedangkan masalah operasional 
           bertanggung jawab kepada Jaksa Agung.</p>
        <p>Kemudian mulai tanggal 1 Juli 1946 dengan Penetapan Pemerintah tahun 1946 No. 11/S.D. 
           Djawatan Kepolisian Negara yang bertanggung jawab langsung kepada Perdana Menteri. Tanggal 1 Juli 
           inilah yang setiap tahun diperingati sebagai Hari Bhayangkara hingga saat ini.</p>
        <p>Sebagai bangsa dan negara yang berjuang mempertahankan kemerdekaan maka Polri di samping bertugas 
           sebagai penegak hukum juga ikut bertempur di seluruh wilayah RI. Polri menyatakan dirinya “combatant” 
           yang tidak tunduk pada Konvensi Jenewa. Polisi Istimewa diganti menjadi Mobile Brigade, 
           sebagai kesatuan khusus untuk perjuangan bersenjata, seperti dikenal dalam pertempuran 10 November di Surabaya, 
           di front Sumatera Utara, Sumatera Barat, penumpasan pemberontakan PKI di Madiun, dan lain-lain.</p>
        <p>Pada masa kabinet presidential, pada tanggal 4 Februari 1948 dikeluarkan Tap Pemerintah No. 1/1948 
           yang menetapkan bahwa Polri dipimpin langsung oleh presiden/wakil presiden dalam kedudukan sebagai 
           perdana menteri/wakil perdana menteri.</p>
        <p>Pada masa revolusi fisik, Kapolri Jenderal Polisi R.S. Soekanto telah mulai menata organisasi kepolisian 
           di seluruh wilayah RI. Pada Pemerintahan Darurat RI (PDRI) yang diketuai Mr. Sjafrudin Prawiranegara 
           berkedudukan di Sumatera Tengah, Jawatan Kepolisian dipimpin KBP Umar Said (tanggal 22 Desember 1948).</p>
        <p>Hasil Konferensi Meja Bundar antara Indonesia dan Belanda dibentuk Republik Indonesia Serikat (RIS), 
           maka R.S. Sukanto diangkat sebagai Kepala Jawatan Kepolisian Negara RIS dan R. Sumanto diangkat sebagai 
           Kepala Kepolisian Negara RI berkedudukan di Yogyakarta.</p>
        <p>Dengan Keppres RIS No. 22 tahun 1950 dinyatakan bahwa Jawatan Kepolisian RIS dalam kebijaksanaan politik 
           polisional berada di bawah perdana menteri dengan perantaraan jaksa agung, sedangkan dalam hal administrasi 
           pembinaan, dipertanggungjawabkan pada menteri dalam negeri.</p>
        <p>Umur RIS hanya beberapa bulan. Sebelum dibentuk Negara Kesatuan RI pada tanggal 17 Agustus 1950, 
           pada tanggal 7 Juni 1950 dengan Tap Presiden RIS No. 150, organisasi-organisasi kepolisian negara-negara bagian 
           disatukan dalam Jawatan Kepolisian Indonesia. Dalam peleburan tersebut disadari adanya kepolisian negara yang 
           dipimpin secara sentral, baik di bidang kebijaksanaan siasat kepolisian maupun administratif, organisatoris.</p>
